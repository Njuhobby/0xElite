const roleLabels = {
  admin: 'Admin',
  developer: 'Developer',
  client: 'Client',
};

const roleColors = {
  admin: 'bg-red-50 text-red-700 border-red-200',
  developer: 'bg-violet-50 text-violet-700 border-violet-200',
  client: 'bg-blue-50 text-blue-700 border-blue-200',
};

export default function DashboardShell({ children, role, navItems, switchRole }: DashboardShellProps) {
  const pathname = usePathname();
  const { address } = useAccount();
  const { disconnect } = useDisconnect();
  const router = useRouter();

  const handleDisconnectAndHome = () => {
    disconnect();
    router.push('/');
  };

  return (
    <div className="min-h-screen bg-gray-50 flex">
      {/* Sidebar */}
      <aside className="w-64 bg-white border-r border-gray-200 flex flex-col fixed h-full z-20">
        {/* Logo */}
        <div className="h-16 flex items-center px-6 border-b border-gray-200">
          <Link href="/" className="flex items-center gap-2">
            <div className="w-8 h-8 rounded-lg bg-gradient-to-br from-violet-600 to-purple-600 flex items-center justify-center">
              <span className="text-white font-bold text-sm">0x</span>
            </div>
            <span className="text-lg font-bold text-gray-900">Elite</span>
          </Link>
          <span className={`ml-auto text-xs font-medium px-2 py-0.5 rounded-full border ${roleColors[role]}`}>
            {roleLabels[role]}
          </span>
        </div>

        {/* Navigation */}
        <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
          {navItems.map((item) => {
            const isActive =
              pathname === item.href ||
              (item.href !== `/dashboard/${role}` && pathname.startsWith(item.href));

            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-violet-50 text-violet-700'
                    : 'text-gray-600 hover:bg-gray-100 hover:text-gray-900'
                }`}
              >
                <span className={isActive ? 'text-violet-600' : 'text-gray-400'}>{item.icon}</span>
                {item.name}
              </Link>
            );
          })}

          {/* Switch role link */}
          {switchRole && (
            <>
              <div className="border-t border-gray-200 my-3" />
              <Link
                href={switchRole.href}
                className="flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium text-gray-600 hover:bg-gray-100 hover:text-gray-900 transition-colors"
              >
                <span className="text-gray-400">{switchRole.icon}</span>
                {switchRole.label}
              </Link>
            </>
          )}
        </nav>

        {/* Bottom section */}
        <div className="border-t border-gray-200 px-3 py-3 space-y-1">
          <button
            onClick={handleDisconnectAndHome}
            className="w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium text-gray-600 hover:bg-gray-100 hover:text-gray-900 transition-colors"
          >
            <span className="text-gray-400">{Icons.home}</span>
            Back to Home
          </button>
        </div>

        {/* Wallet info */}
        <div className="border-t border-gray-200 px-4 py-3">
          <div className="flex items-center gap-3">
            <div className="w-8 h-8 rounded-full bg-gradient-to-br from-violet-500 to-purple-600 flex items-center justify-center text-white text-xs font-bold shrink-0">
              {address?.slice(2, 4).toUpperCase()}
            </div>
            <div className="min-w-0 flex-1">
              <p className="text-sm font-medium text-gray-900 truncate">
                {address?.slice(0, 6)}...{address?.slice(-4)}
              </p>
              <p className="text-xs text-gray-500">Connected</p>
            </div>
          </div>
        </div>
      </aside>

      {/* Main content area */}
      <div className="flex-1 ml-64">
        {/* Top bar */}
        <header className="h-16 bg-white border-b border-gray-200 px-8 sticky top-0 z-10 flex items-center justify-end">
          {address && <NotificationBell address={address} />}
        </header>

        {/* Page content */}
        <main className="p-8">
          {children}
        </main>
      </div>
    </div>
  );
}
